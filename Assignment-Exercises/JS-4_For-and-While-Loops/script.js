// for loop to iterate numbers from 1 to 10
for (let i = 1; i <= 10; i++) {
  console.log(`Number: ${i}`);
}

// while loop to print a list of hobbies
let favoriteFoods = [
  "Pasta",
  "Pizza",
  "Ice Cream",
  "Chocolate",
  "Sinigang",
  "Adobo",
  "Fried Chicken",
  "Fruit Shake",
  "Bingsu",
  "Tinola",
];

let index = 0;
while (index < favoriteFoods.length) {
  console.log(`Favorite Food: ${favoriteFoods[index]}`);
  index++;
}
